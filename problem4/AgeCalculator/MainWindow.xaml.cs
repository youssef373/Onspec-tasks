namespace AgeCalculator
{
    /// <summary>
    /// Interaction logic for MainWindow.xaml
    /// </summary>
    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
        }

        private void CalculateAgeButton_Click(object sender, RoutedEventArgs e)
        {
            if (DatePicker.SelectedDate == null) return;

            TextBlock.Text = GetAgeString(DatePicker.SelectedDate.Value);
        }

        public string GetAgeString(DateTime dob)
        {
            DateTime today = DateTime.Today;

            int months = today.Month - dob.Month;
            int years = today.Year - dob.Year;

            if (today.Day < dob.Day)
            {
                months--;
            }

            if (months < 0)
            {
                years--;
                months += 12;
            }

            int days = (today - dob.AddMonths((years * 12) + months)).Days;

            return $"Your current age is {years} years, {months} months and {days} days";
        }
    }
}
